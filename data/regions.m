function regions = regions(nordic)
%REGIONS The official names of the 21 Swedish regions. If NORDIC is
% give as true no input is given, then return the list without the
% nordic caracters.

if nargin == 0 | nordic
    regions = {'Stockholm' 'Uppsala' 'Södermanland' 'Östergötland' ...
               'Jönköping' 'Kronoberg' 'Kalmar' 'Gotland' 'Blekinge' ...
               'Skåne' 'Halland' 'Västra Götaland' 'Värmland' 'Örebro' ...
               'Västmanland' 'Dalarna' 'Gävleborg' 'Västernorrland' ...
               'Jämtland' 'Västerbotten' 'Norrbotten'}';
else
    regions = {'Stockholm' 'Uppsala' 'Sodermanland' 'Ostergotland' ...
               'Jonkoping' 'Kronoberg' 'Kalmar' 'Gotland' 'Blekinge' ...
               'Skane' 'Halland' 'Vastra Gotaland' 'Varmland' 'Orebro' ...
               'Vastmanland' 'Dalarna' 'Gavleborg' 'Vasternorrland' ...
               'Jamtland' 'Vasterbotten' 'Norrbotten'}';
end
end
